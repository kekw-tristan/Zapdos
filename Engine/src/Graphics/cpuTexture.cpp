#include "cpuTexture.h"

// --------------------------------------------------------------------------------------------------------------------------

cCpuTexture::cCpuTexture(int _width, int _height, std::vector<uint8_t> _data, DXGI_FORMAT _format)
	: m_width(_width)
	, m_height(_height)
	, m_data(_data)
	, m_format(_format)
{
}

// --------------------------------------------------------------------------------------------------------------------------

cCpuTexture::~cCpuTexture()
{
}

// --------------------------------------------------------------------------------------------------------------------------

std::vector<uint8_t>& cCpuTexture::GetData()
{
	return m_data; 
}

// --------------------------------------------------------------------------------------------------------------------------

int cCpuTexture::GetWidth()
{
	return m_width;
}

// --------------------------------------------------------------------------------------------------------------------------

int cCpuTexture::GetHeight()
{
	return m_height;
}

DXGI_FORMAT cCpuTexture::GetFormat()
{
	return m_format;
}

